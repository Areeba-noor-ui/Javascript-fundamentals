/* task 1 Create a for and while loop that counts up from 0 to 10, but counts up by 2 (0, 2, 4, ... , 8, 10).*/

for (let i=0 ; i<=10 ; i+=2){
    console.log(i);
}

console.log('while loop')
let i=0;
while(i<=10){
    console.log(i)
    i+=2;
}

